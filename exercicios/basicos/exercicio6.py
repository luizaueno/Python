# Leia um número, calcule e mostre o dobro triplo e raiz quadrada

n = int(input(' Digite um número: '))
d = n * 2
t = n * 3
r = n ** 1/2 # o contrário de elevar ao quadrado 

print('O dobro do número informado é: {}'.format(d))
print('O triplo do número informado é: {}'.format(t))
print('A raiz quadrada do número informado é {}'.format(r))